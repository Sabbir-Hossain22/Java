
package sabbir.anisulislamvideo;


public class Loop1Demo {
    
    public static void main(String[] args) {
        int i , sum =0;
        
        for ( i = 1; i <= 10; i++) 
        {
            sum = sum + i;
        }
        System.out.println("The sum of all numbers between 1 to 10 is : "+sum);
    }
    
}
