public class BitwiseOperator {
    public static void main(String[] args) {
        
        int a =32;
        int b =12;
        int c ;
        
        c = a & b ;
        System.out.println("Bitwise AND : "+c);
        
        c = a | b ;
        System.out.println("Bitwise OR : "+c);
        
        c = a ^ b ;
        System.out.println("Bitwise XOR : "+c);
        
    }
    
}
